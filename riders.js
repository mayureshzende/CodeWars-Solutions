function riders(stations) {
  let count = 0,
    rider = 1;
  for (let i = 0; i < stations.length; i++) {
    if (count + stations[i] > 100) {
      count = stations[i];
      console.log(`count is ${count} at  location ${i} and rider is ${rider}`);
      ++rider;
    } else {
      count += stations[i];
    }
  }

  return rider;
}

